#2D lists = list of lists

drinks = ["coffee","soda","tea"]
dinner = ["pizza","hamburger","hotdog"]
dessert = ["cake","ice cream"]

food = [drinks,dinner,dessert] #stored all 3 lists inside a variable called food.

print(food) #calls all elements inside the variable food.

print(food[0]) #specify which list you'd like to display.

print(food[0][1]) #specify which element inside of list you'd like to display.