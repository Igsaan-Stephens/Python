# For loop = Statement that executes its block of code a limited amount of times.
# while loop = unlimited
# for loop = limited
import time
for i in range(50 , 100+1 , 2):
    print(i)

for i in "Igsaan Stephens":
    print(i)

for seconds in range(10 , 0 , -1):
    print(seconds)
    time.sleep(1)
